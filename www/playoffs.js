function eventAlliancesToCsv(){
	// Only emit the Backup column when every alliance actually has a backup, so the
	// header and rows always line up (a blank/undefined backup would make a row
	// short and the server would reject the CSV as malformed).
	var includeBackup = fourTeam && eventAlliances.length > 0 &&
		eventAlliances.every(function(a){ return a['Backup'] !== undefined && a['Backup'] !== '' })
	var headings = []
	$('#alliancesHead th').each(function(){
		if (!includeBackup && $(this).hasClass('backupCol')) return
		headings.push(translate($(this).attr('data-i18n'),{},'en')||$(this).text())
	})
	var csv = headings.join(',')+"\n"
	for (var i=0; i<eventAlliances.length; i++){
		for (var j=0; j<headings.length; j++){
			if (j>0) csv += ","
			var v = eventAlliances[i][headings[j]]
			csv += (v === undefined || v === null) ? '' : v
		}
		csv += "\n"
	}
	return csv
}

