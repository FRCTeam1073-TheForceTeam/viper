// Whether alliances carry a 4th member (a backup robot). The backup is part of
// the roster but never plays in the 3v3 matches, so it is excluded from the
// schedule and from predictions.
var fourTeam = false

function tableToCsv(table){
	// Include the backup column only for a complete 4-team roster: if the Backup
	// header is present but any row's backup is blank, that row has fewer team
	// numbers than there are columns and the server rejects the whole CSV as
	// malformed. Requiring every backup to be filled keeps header and rows aligned.
	var includeBackup = table.hasClass('fourTeam') && table.find('input.pick_3').filter(withoutValues).length === 0
	var csv=""
	table.find('tr').each(function(){
		var line=""
		$(this).find('th,td').each(function(){
			if (!includeBackup && $(this).hasClass('backupCol')) return
			if (line) line += ","
			line += $(this).attr('data-csv') || $(this).text() || $(this).find('input').val() || ""
		})
		csv += line + "\n"
	})
	return csv
}

