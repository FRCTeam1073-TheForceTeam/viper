function savePlayoffs(){
	// Guard against saving a 4-team roster with any empty backup: that produces a
	// row with fewer team numbers than the header has columns, which the server
	// rejects as a malformed CSV.
	if (fourTeam && $('#alliances').is(':visible') && $('#alliances .pick_3').filter(withoutValues).length){
		alert(translate('fill_backups_message'))
		return false
	}
	$('#eventInput').val(eventId)
	addAlliancesHiddenFields()
	var regenerating = $('#alliances').is(':visible')
	$('#alliancesCsvInput').val(regenerating?tableToCsv($('#alliances').closest('table')):eventAlliancesToCsv())
	if ($('#scheduleTable').is(':visible')){
		$('#scheduleCsvInput').val(tableToCsv($('#scheduleTable')))
	}
	// Saving from the alliance-entry screen means the bracket was (re)generated, so
	// the server should drop any previously generated playoff matches and replace
	// them with this fresh schedule rather than merging round-by-round.
	$('#resetScheduleInput').val(regenerating?'1':'')
	// Submit in the background so we stay on playoffs.html instead of
	// navigating away to the server's redirect target.
	var form = $('#addAlliances')
	$('#save,#saveBracket').prop('disabled', true)
	$.ajax({url:form.attr('action'), method:'POST', data:form.serialize()})
		.done(function(){
			// Stay on the page and transition to the saved bracket; the
			// reveal animation plays because the bracket was hidden.
			showContent()
		})
		.fail(function(xhr){
			// Log the exact CSV we sent so a rejected save can be diagnosed.
			console.error('Playoffs save rejected. alliancesCsv sent:\n' + $('#alliancesCsvInput').val())
			var m = (xhr.responseText||'').match(/<h1[^>]*>([^<]*)<\/h1>/)
			alert(m ? m[1] : 'Save failed')
		})
		.always(function(){ $('#save,#saveBracket').prop('disabled', false) })
	return false
}

// History of winner picks made this session, so a mis-click can be undone.
var winnerHistory = []

// Record (and apply) a winner selection, remembering the previous values so it
// can be reverted by undoWinner.
function recordWinner(aInd, oppInd, column){
	winnerHistory.push({
		aInd:aInd, oppInd:oppInd, column:column,
		prevA:eventAlliances[aInd][column], prevB:eventAlliances[oppInd][column]
	})
	eventAlliances[aInd][column] = 1
	eventAlliances[oppInd][column] = 0
	updateUndoButton()
}

// Revert the most recent winner pick and re-render the bracket. Save afterwards
// to persist the corrected result.
function undoWinner(){
	var last = winnerHistory.pop()
	if (!last) return
	restoreColumn(eventAlliances[last.aInd], last.column, last.prevA)
	restoreColumn(eventAlliances[last.oppInd], last.column, last.prevB)
	updateUndoButton()
	showContent()
}

function restoreColumn(alliance, column, prev){
	if (prev === undefined) delete alliance[column]
	else alliance[column] = prev
}

function updateUndoButton(){
	$('#undoWinner').prop('disabled', !winnerHistory.length)
}

