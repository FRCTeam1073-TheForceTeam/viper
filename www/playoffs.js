function withoutValues(i,el){
	return $(el).val() == ''
}

// Grey out exactly the team buttons whose number is currently used in some
// alliance slot, recomputed from scratch so replacing a team in a slot frees the
// old team to be picked again.
function refreshPickedTeams(){
	var used = {}
	$('#alliances input').each(function(){
		var v = $(this).val()
		if (v) used[v] = true
	})
	$('#teams .team').each(function(){
		$(this).toggleClass('picked', !!used[$(this).text()])
	})
}

function focusNext(){
	var next = $('#alliances .captain, #alliances .pick_1').filter(withoutValues).first()
	if (!next.length) next = $('#alliances .pick_2').filter(withoutValues).last()
	if (!next.length && fourTeam) next = $('#alliances .pick_3').filter(withoutValues).first()
	if (next.length) focusInput(next)
	return next.length > 0
}
