// Pull the alliance selection from the official API data (alliances.json, written
// by the FIRST/Blue Alliance fetch), fill the entry form, and — after the user
// confirms — save it to the bracket. Captain/round1/round2/round3 map to
// captain/first pick/second pick/backup.
function importApiAlliances(){
	fetch(`/data/${eventId}.alliances.json`)
		.then(r => r.ok ? r.json() : null)
		.then(data => {
			var alliances = ((data && data.Alliances) || []).filter(a => a && a.number && a.captain)
			if (!alliances.length){ alert(translate('no_api_alliances')); return }
			alliances.sort((a,b) => a.number - b.number)
			// Use 4-team mode only if every alliance has a backup, so the backup
			// column stays consistent across all rows.
			fourTeam = alliances.every(a => a.round3)
			$('#fourTeamToggle').prop('checked', fourTeam)
			$('.allianceTable').toggleClass('fourTeam', fourTeam)
			alliances.forEach(function(a){
				$('#A'+a.number+'_captain').val(a.captain || '')
				$('#A'+a.number+'_pick_1').val(a.round1 || '')
				$('#A'+a.number+'_pick_2').val(a.round2 || '')
				$('#A'+a.number+'_pick_3').val(fourTeam ? (a.round3 || '') : '')
			})
			refreshPickedTeams()
			// Only offer to save once every required slot is filled.
			if (focusNext()){ $('#scheduleSection').hide(); alert(translate('import_api_incomplete')); return }
			showImportConfirm(alliances)
		})
		.catch(() => alert(translate('no_api_alliances')))
}

